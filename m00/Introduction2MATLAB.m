%[text] - 이미지 분류 간단 실습: 전이학습 없이 작은 CNN으로 교과목 관련 이미지 분류.
%[text] - 신호 분석 과제: 마이크로폰 음성 신호에서 단어 길이 추출 및 시각화.
%[text] - Simulink 연동: 간단한 PID 제어 실험 모델 만들고 시뮬레이션 결과 비교. \
