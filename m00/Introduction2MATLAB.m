%[text] - 목표: 모델 기반 설계의 기본 이해. \
%[text] 8주: 미니 프로젝트 완성 및 문서화
%[text] - 실제 데이터(수업 과제나 공개 데이터)로 1개 미니 프로젝트 진행(데이터 전처리→모델→평가).
