%[text] 당장에 내가 풀어볼 수 있는 쉬운 문제 5가지를 알려줘
%[text] 1\) 사인파 그리기와 저장
%[text] - 목표: 시간축에 따른 사인파를 그리고 이미지 파일로 저장.
%[text] - 작업: t 벡터 만들기, y = sin(2πft) 계산, plot으로 그리기, xlabel/ ylabel/ title 추가, saveas로 PNG 저장.
%[text] - 힌트: \
t = 0:0.01:2*pi;
y = sin(t);
plot(t,y)

%[text] 2\) CSV 파일의 기본 통계 구하기
%[text] - 목표: CSV 파일(한 열 또는 여러 열)에서 평균·중앙값·표준편차 구하기.
%[text] - 작업: readmatrix 또는 readtable로 불러오기, mean/median/std 적용, 결과 출력.
%[text] - 힌트: \
data = readmatrix('data.csv');    % 또는 임의 데이터: data = randn(100,1);
m = mean(data);
med = median(data);
s = std(data);

%[text] 3\) 간단한 이미지 처리: 흑백 변환 및 엣지 검출
%[text] - 목표: 컬러 이미지 불러와 그레이스케일 변환 후 엣지 검출 결과 보기.
%[text] - 작업: imread, rgb2gray, edge 사용, subplot으로 원본·결과 나란히 표시.
%[text] - 힌트: \
I = imread('image.jpg');
G = rgb2gray(I);
E = edge(G,'Canny');
imshowpair(G,E,'montage')

%[text] 4\) 랜덤 숫자 배열 정렬하고 상위 5개 출력
%[text] - 목표: 100개 난수 생성, 내림차순 정렬 후 상위 5개와 인덱스 보여주기.
%[text] - 작업: randn 또는 rand로 배열 생성, sort 또는 maxk 사용, 결과 출력.
%[text] - 힌트: \
x = randn(100,1);
[xs, idx] = sort(x,'descend');
top5 = xs(1:5);

%[text] 5\) 간단한 선형회귀(직선 피팅) 및 예측
%[text] - 목표: x-y 데이터에 직선 y = a\*x + b를 피팅하고 새 x에 대한 예측 수행.
%[text] - 작업: polyfit 또는 regress로 계수 구하기, plot으로 데이터와 직선 표시, polyval로 예측.
%[text] - 힌트: \
x = (1:10)';
y = 2*x + 3 + randn(10,1);   % 예제 데이터
p = polyfit(x,y,1);
yfit = polyval(p,x);
plot(x,y,'o',x,yfit,'-')
